import java.util.*;

/*
 * Program: Sum of Two Numbers
 * ---------------------------
 * This program calculates the sum of two integers and prints the result.
 */

public class _0002_SumOfNumber {
    public static void main(String args[]) {
        int a = 6;  // Assign value to variable a
        int b = 9;  // Assign value to variable b

        int sum = a + b;  // Add values of a and b

        System.out.println(sum);  // Print the sum
    }
}
